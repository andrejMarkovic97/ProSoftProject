    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtFirstName.getText().isEmpty() || txtLastName.getText().isEmpty()
                || txtUsername.getText().isEmpty() || txtPassword.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "All fields must be filled in before submitting!", "Submit error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Employee employee = new Employee();
            employee.setFirstName(txtFirstName.getText());
            employee.setLastName(txtLastName.getText());
            employee.setUsername(txtUsername.getText());
            employee.setPassword(String.valueOf(txtPassword.getPassword()));
            employee.setRole((String) cmbRole.getSelectedItem());
            employee.setRentals(new ArrayList<Rental>());
            boolean success = ClientController.getInstance().addEmployee(employee);
            if (success) {
                JOptionPane.showMessageDialog(this, "Employee successfully added!");
                fillEmployeeTable();

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Operation error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed
    }
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblEmployee.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "No employee selected!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this employee?", "Confirm deletion", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.NO_OPTION) {
            return;
        }

        if (option == JOptionPane.YES_OPTION) {
            TableModelEmployee tbl = (TableModelEmployee) tblEmployee.getModel();
            try {
                tbl.delete(row);
                JOptionPane.showMessageDialog(this, "Employee successfully deleted!");
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", HEIGHT);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

